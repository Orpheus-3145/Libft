#include "libft.h"

char	ft_bin_to_char(int *bin_char)
{
	char	c;
	size_t	i;

	c = 0;
	i = 0;
	while (i < 8)
	{
		c += ft_pow(2, 8 - i - 1) * bin_char[i];
		i++;
	}
	return (c);
}
