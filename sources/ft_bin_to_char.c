#include "libft.h"

char	ft_bin_to_char(int *bin_char, size_t bits)
{
	char	c;
	size_t	i;

	c = 0;
	i = 0;
	while (i < bits)
	{
		c += ft_pow(2, bits - i - 1) * bin_char[i];
		i++;
	}
	return (c);
}
